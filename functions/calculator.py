def addition(num1, num2): #define function and add arguments
    return num1 + num2 #returns argument behaviour (adds both numbers)
print(addition(5, 3))#provides parameters for num1 and num2

def subtraction(num1, num2):
    return num1 - num2
print(subtraction(11, 4))


def multiplication(num1, num2):
    return num1 * num2
print(multiplication(12, 6))

def division(num1, num2):
    return num1 / num2
print(division(50, 4))
